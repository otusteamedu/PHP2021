CREATE OR REPLACE PROCEDURE insert_dynamic_data(num_of_entries integer)
LANGUAGE plpgsql AS
$$
    DECLARE
        film_id INTEGER;
        hall_id INTEGER;
        film_duration INTERVAL;
        start_date TIMESTAMP := current_date::TIMESTAMP - interval '2.8 years';
        price_id INTEGER := 1;
        ticket_id INTEGER := 1;
        row_start INTEGER;
        row_end INTEGER;
        seat_start INTEGER;
        seat_end INTEGER;
        current_price INTEGER;
        ticket_status INTEGER;
        ticket_price NUMERIC(5,2);
    BEGIN
        SET TIMEZONE TO 'Europe/Moscow';
        FOR i IN 1..num_of_entries LOOP
--             i - session_id
            film_id := ROUND(random() * 2 + 1);
            hall_id := ROUND(random() + 1);
            film_duration := trunc(random() * 3 + 1) * '1 hours'::interval;
            INSERT INTO public.session (id, dt_start, dt_end, film_id, hall_id) VALUES(i, start_date, start_date + film_duration, film_id, hall_id);
            start_date := start_date + film_duration + interval '0.5 hour';

            IF hall_id = 1 THEN
                row_start := 1;
                row_end := 45;
                seat_start := 1;
                seat_end := 450;
                current_price := 500;
            ELSE
                row_start := 46;
                row_end := 55;
                seat_start := 451;
                seat_end := 570;
                current_price := 400;
            END IF;

            IF price_id < num_of_entries THEN
                FOR j IN row_start..row_end LOOP
--                 j - row_id
                    INSERT INTO public.price (id, session_id, row_id, price) VALUES(price_id, i, j, current_price);

                    IF ticket_id < num_of_entries THEN

                        SELECT
                            MIN(s.id) INTO seat_start
                        FROM public.seat s
                        WHERE s.row_id = j;

                        SELECT
                            MAX(s.id) INTO seat_end
                        FROM public.seat s
                        WHERE s.row_id = j;

                        FOR k IN seat_start..seat_end LOOP
                            ticket_price := 0;
                            ticket_status := ROUND(random() * 3 + 1);
                            IF ticket_status IN(2, 3) THEN
                                ticket_price := ((100 - ROUND(random() * 3) * 5) / 100) * current_price;
                            END IF;
                            INSERT INTO public.ticket (id, seat_id, price_id, status_id, price_fact) VALUES(ticket_id, k, price_id, ticket_status, ticket_price);
                            ticket_id := ticket_id + 1;
                        END LOOP;
                    END IF;

                    price_id := price_id + 1;
                END LOOP;
            END IF;

        END LOOP;
    END;
$$;

